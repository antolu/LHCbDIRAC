class Quality:
  
  #############################################################################
  def __init__(self):
    self.group_ = ""
    self.flag_ = ""
    self.qualityID_ = -1
    self.params_ = []
    
  #############################################################################
  def setGroup(self, name):
    self.group_ = name
  
  #############################################################################
  def getGroup(self):
    return self.group_
  
  #############################################################################
  def setFlag(self, flag):
    self.flag_ = flag
  
  #############################################################################
  def getFlag(self):
    return self.flag_

  #############################################################################
  def addParam(self, param):
    self.params_ += [param]
    
  #############################################################################
  def getParams(self):
    return self.params_
  
  #############################################################################
  def setQualityID(self, id):
    self.qualityID_ = id
  
  #############################################################################
  def getQualityID(self):
    return self.qualityID_
  
  #############################################################################
  def __repr__(self):
    result = "Quality: "
    result += self.group_ + " " + self.flag_ + "\n"
    
    for param in self.params_:
      result += str(param)
    
    result += "\n"
    return result
 
  #############################################################################  
  def writeToXML(self):
    result = '<Quality Group="'+ self.getGroup()+'" Flag="'+ self.getFlag()+'"/>\n'
    return result
    
  
