class FileParam:
    

  #############################################################################  
  def __init__(self):
    self.name_ = ""
    self.value_ = ""
      
  #############################################################################  
  def setParamName(self, name):
    self.name_ = name
      
  #############################################################################  
  def getParamName(self):
    return self.name_

  #############################################################################  
  def setParamValue(self, value):
    self.value_ = value
      
  #############################################################################  
  def getParamValue(self):
    return self.value_
  
  #############################################################################  
  def __repr__(self):
    result = '\nFileParam: \n'
    result += self.name_ + ' ' + self.value_ + '\n'
    return result
  
